
public class IfTest01 {

	public static void main(String[] args) {
		// 조건문 - if  { }
		int score =85;
		
		if(score >=90) {
			System.out.println("점수가 90점 보다 큽니다");
			System.out.println("등급이 A입니다");
		} else {
			System.out.println("점수가 90점 보다 작습니다");
			System.out.println("등급이 B입니다");
		}
	}

}
